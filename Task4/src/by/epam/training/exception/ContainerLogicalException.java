package by.epam.training.exception;

public class ContainerLogicalException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ContainerLogicalException() {
		super();
	}

	public ContainerLogicalException(String message, Throwable cause) {
		super(message, cause);
	}

	public ContainerLogicalException(String message) {
		super(message);
	}

	public ContainerLogicalException(Throwable cause) {
		super(cause);
	}

}
